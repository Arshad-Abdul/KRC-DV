// Get all faculty members for a department
export const getFacultyByScopeIds = (dept) => {
  return facultyByDept[dept] || [];
};

// Get all Scopus IDs for a department (filter out NA values and handle multiple IDs)
export const getScopusIdsByDept = (dept) => {
  const faculty = facultyByDept[dept] || [];
  const scopusIds = [];
  
  faculty.forEach(f => {
    if (f.scopusId && f.scopusId !== 'NA') {
      // Handle multiple IDs separated by semicolon
      if (f.scopusId.includes(';')) {
        const multipleIds = f.scopusId.split(';').map(id => id.trim());
        scopusIds.push(...multipleIds);
      } else {
        scopusIds.push(f.scopusId);
      }
    }
  });
  
  return scopusIds.filter(id => id && id !== 'NA');
};

// Get total faculty count across all departments
export const getTotalFacultyCount = () => {
  return Object.values(facultyByDept).reduce((total, dept) => total + dept.length, 0);
};

// Get faculty count by department
export const getFacultyCountByDept = (dept) => {
  return facultyByDept[dept]?.length || 0;
};
